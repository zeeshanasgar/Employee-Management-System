        public override string[] GetRolesForUser(string username)
        {
            using (var context = new EmployeeDBEntities())
            {
                var result = (from signup in context.EmployeeInfoStore
                              join role in context.EmployeeRole on signup.EmpId equals role.EmpId
                              where signup.username == username
                              select role.Role).ToArray();
                return result;
            }
        }

